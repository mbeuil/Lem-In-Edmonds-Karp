#include "printf.h"
#include "../libft/libft.h"

void	ft_printf_width_pad(int nbrstrlen, int width, char padwith)
{
	while (nbrstrlen < width)
	{
		ft_putchar(padwith);
		nbrstrlen++;
	}
}
