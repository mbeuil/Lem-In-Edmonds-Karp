#include "libft.h"

int					ft_isnumber(char *str)
{
	int				i;

	if (!str || ft_strcmp(str, "-") == 0)
		return (0);
	i = 0;
	if (str[0] == '-')
		i++;
	while (str[i])
	{
		if (!ft_isdigit(str[i]))
			return (0);
		i++;
	}
	return (1);
}
