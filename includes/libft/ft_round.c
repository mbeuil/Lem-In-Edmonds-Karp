#include "libft.h"

double		ft_round(double x)
{
	double	y;

	if (x >= 0)
	{
		y = ft_floor(x);
		if (x - y >= 0.5)
			y += 1;
	}
	else
	{
		y = ft_ceil(x);
		if (y - x >= 0.5)
			y -= 1;
	}
	return (y);
}
