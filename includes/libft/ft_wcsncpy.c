#include "libft.h"

wchar_t			*ft_wcsncpy(wchar_t *s1, const wchar_t *s2, size_t len)
{
	size_t		i;

	i = 0;
	while (s2[i] != L'\0' && i < len)
	{
		s1[i] = s2[i];
		i++;
	}
	while (i < len)
		s1[i++] = L'\0';
	return (s1);
}
