#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	j;
	size_t	k;

	i = 0;
	j = 0;
	k = 0;
	while (src[k] != '\0')
		k++;
	if (size == 0)
		return (k);
	while (dst[i] != '\0')
		i++;
	while ((i + j) < (size - 1) && src[j] != '\0')
	{
		dst[i + j] = src[j];
		j++;
	}
	dst[i + j] = '\0';
	if ((k + size) <= (k + i))
		return (k + size);
	else
		return (k + i);
}
